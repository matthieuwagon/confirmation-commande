// ── Fonctions de calcul ──────────────────────────────────────────────────────
export function calcLivraisonSerenite(products, departement) {
  const zone = ZONE_FRANCE[String(departement).padStart(2, "0")];
  if (!zone) return null;
  const idx = zone - 1;
  return products.reduce((total, p) => {
    const tarifs = SERENITE_TARIF[p.modeleId];
    if (!tarifs) return total;
    return total + tarifs[idx] * (p.quantite || 1);
  }, 0);
}

export function calcLivraisonConfort(products, departement) {
  const dept = String(departement).padStart(2, "0");
  const tarifs = CONFORT_TARIF[dept];
  if (!tarifs) return null;
  let total = 0;
  for (const p of products) {
    const key = CONFORT_KEY[p.modeleId];
    if (!key) return null; // modèle non tarifé (ex: SIXO) → sur devis
    total += tarifs[key] * (p.quantite || 1);
  }
  return total;
}

export function calcRemise(totalBrut, remisePct) {
  const taux = parseFloat(remisePct) || 0;
  return Math.round(totalBrut * taux / 100);
}
